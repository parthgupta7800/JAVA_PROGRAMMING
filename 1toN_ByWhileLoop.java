import java.util.*;

class natural {
    public static void main(String args[]) {
        Scanner ob = new Scanner(System.in);
        int n;
        System.out.println("Enter the limit that is value of n");
        n = ob.nextInt();
        int i = 1;
        while (i <= n) {
            System.out.println(i);
            i++;
        }
    }
}