import java.util.*;

class natural2 {
    public static void main(String args[]) {
        Scanner ob = new Scanner(System.in);
        int n, i;
        System.out.println("Enter the number");
        n = ob.nextInt();
        for (i = 1; i <= n; i++) {
            System.out.println(i);
        }
    }
}