import java.util.*;

class natural1 {
    public static void main(String args[]) {
        Scanner ob = new Scanner(System.in);
        int n;
        System.out.println("Enter the Number");
        n = ob.nextInt();
        while (n > 0) {
            System.out.println(n);
            n--;
        }
    }
}