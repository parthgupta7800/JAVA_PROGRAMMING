import java.util.*;

class even {
    public static void main(String args[]) {
        Scanner ob = new Scanner(System.in);
        System.out.println("Enter the number");
        int a = ob.nextInt();
        if (a % 2 == 0) {
            System.out.println("a is even");
        } else
            System.out.println("a is odd");

    }
}